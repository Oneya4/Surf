class Destination {
  final String id;
  final String imageUrl;
  final String name;
  final String location;
  final String description;
  final String waterFlow;
  final double cost;

  Destination({
    required this.id,
    required this.imageUrl,
    required this.name,
    required this.location,
    required this.description,
    required this.waterFlow,
    required this.cost,
  });
}
